			<h1>Top tips for entrants</h1>
			<p>Know your topic inside out. Read and research! Go out there and speak to people!</p>
			<p>Don’t go for the obvious. Even if your topic is a well-covered one, consider what new light you are shedding on it. Make it investigative and think of the impact it will have on real lives. </p>
